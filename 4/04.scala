/*
 * Option들의 목록을 받고, 그 목록에 있는 모든 Some 값으로 구성된 목록을 담은 Option을 돌려주는 함수 sequence를 작성하라.
 * 원래의 목록에 None이 하나라도 있으면 함수의 결과도 None이어야 한다.
 * 그렇지 않으면 원래의 목록에 있는 모든 값의 목록을 담은 Some을 돌려주어야 한다.
 */

def sequence[A](a: List[Option[A]]): Option[List[A]] = a.foldRight(Some(Nil): Option[List[A]])((e, acc) => for (
  accv <- acc;
  ev <- e
) yield ev :: accv)
